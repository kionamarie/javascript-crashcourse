// if, else if

const age = 15;

if (age > 18){
    console.log('You may enter!');
} else if (age === 18){
    console.log('You just turned 18. Welcome!');
} else {
    console.log('Go home Roger!');
}

// While loop
let i = 0;
while (i < 5) {
    console.log(i)
    i++;
}


// For Loop
// for([initialization];[condition];[final-expression])

for(let i = 0; i < 10; i++){
    console.log(i);
}
